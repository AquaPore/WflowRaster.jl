module utils

	# ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	#		FUNCTION : CREATE_PATH
	# ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
	function CREATE_PATH(PathCreated)
		try
			rm(PathCreated, recursive=true)
		catch
			print("")
		end
		mkpath(PathCreated)
	end  # function: CREATE_PATH
	# ------------------------------------------------------------------

end  # module: utils
